// 記事 24 章「MFC DLLとモジュール状態」のコード断片。
//
// MFC DLL からリソースを読み込む・ダイアログを表示する場合、
// どのモジュールのリソースを見に行くか（モジュール状態）が問題になる。
// MFC DLL のエクスポート関数の入口では AFX_MANAGE_STATE マクロを置く。
//
// このマクロを忘れると、「DLL 内のダイアログリソースが見つからない」
// 「文字列リソースが別モジュールから読まれる」「デバッグ時だけ動いて
// リリースで壊れる」といった不具合が起きることがある。
//
// ※ 参照用スニペットです。単体ではビルドできません。

#include <afxwin.h>

void ExportedFunction()
{
    AFX_MANAGE_STATE(AfxGetStaticModuleState());

    // ここから先で DLL 側のリソースを使った処理を行う
}
