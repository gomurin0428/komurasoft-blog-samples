// 記事 28 章「高DPI対応と現代のWindows」のコード断片。
//
// 固定ピクセル前提の座標は、DPI が変わると見た目が崩れる。
// 高 DPI 対応では、アプリケーションマニフェストの DPI 設定、
// ダイアログリソースのフォント、固定ピクセル描画、画像リソースの解像度、
// 複数モニターでの動作を確認する。
//
// ※ 参照用スニペットです。単体ではビルドできません。

#include <afxwin.h>

void DrawExample(CDC* pDC)
{
    // 高 DPI で問題になりやすい固定ピクセル前提の描画
    pDC->TextOut(10, 10, _T("Status"));
    pDC->Rectangle(10, 40, 200, 80);
}
